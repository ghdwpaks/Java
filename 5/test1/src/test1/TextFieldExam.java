package test1;

import java.awt.*;
public class TextFieldExam extends Frame{
	public TextFieldExam(){
		super("텍스트 필드");
		Panel p=new Panel();
		TextField tf1=new TextField("주민등록번호앞자리"); //기본 문자 출력
		TextField tf2=new TextField(15); //15개 문자 보여주는 크기로 생성
		tf1.selectAll(); //입력된 문자열 전체를 선택 상태로 만든다
		tf2.setEchoChar('*'); //입력된 문자를 *로 변환하여 출력한다
		p.add(tf1);
		p.add(tf2);
		add(p);
		setSize(400, 100);
		setVisible(true);
	}
	public static void main(String[] args){
		new TextFieldExam();
	}
}